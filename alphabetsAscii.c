#include <stdio.h>

int main() {
    
    // This prints upperCase alphabets using ASCII values
    for ( int ascii = 64; ascii <= 90; ascii++ ) {
        
        printf("%c ", ascii);
    }
    
    // This prints lowerCase alphabets using ASCII values
    for ( int ascii = 97; ascii <= 122; ascii++ ) {
        
        printf("%c ", ascii);
    }
    
    
}
